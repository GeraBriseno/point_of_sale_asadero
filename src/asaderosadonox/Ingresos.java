    private void b_agregarIngresoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_agregarIngresoMouseClicked
        String concepto = new JOptionPane().showInputDialog("Ingresa el concepto: ");
        Double monto = Double.parseDouble(new JOptionPane().showInputDialog("Ingresa el monto: "));
        Conexion.agregarIngreso(concepto, monto);
        Conexion.mostrarIngresos(t_ingresos);
    }//GEN-LAST:event_b_agregarIngresoMouseClicked

    private void b_eliminarIngresoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_eliminarIngresoMouseClicked
        int columnaConcepto = 0;
        int row = t_ingresos.getSelectedRow();
        String ingresoAEliminar = t_ingresos.getModel().getValueAt(row, columnaConcepto).toString();
        Conexion.eliminarIngreso(ingresoAEliminar);
        Conexion.mostrarIngresos(t_ingresos);
    }//GEN-LAST:event_b_eliminarIngresoMouseClicked

